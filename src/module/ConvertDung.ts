export class ConvertDungeon {
    // Function to convert string to RoomType enum
    static stringToRoomType(roomTypeString: string): RoomType {
        if (!roomTypeString) return 0;
        switch (roomTypeString) {
            case "TELEPORTER": return RoomType.TELEPORTER;
            case "STONE": return RoomType.STONE;
            case "OPEN": return RoomType.OPEN;
            // ... handle other room types
            default: throw new Error("Invalid room type string");
        }
    }

    static extractDetails(line: string) {
        const locationMatch = line.match(/location=\{\s*(-?\d+),\s*(-?\d+),\s*(-?\d+)\s*\}/);
        const flagsMatch = line.match(/flags=0x(\w+)/);
        const roomTypeMatch = line.match(/roomType="(\w+)"/);
    
        if (!locationMatch || !flagsMatch || !roomTypeMatch) {
            throw new Error("Failed to extract details from line");
        }
    
        const lvl = parseInt(locationMatch[1], 10);
        const x = parseInt(locationMatch[2], 10);
        const y = parseInt(locationMatch[3], 10);
    
        const flags = parseInt(flagsMatch[1], 16);  // assuming flags are in hexadecimal

        const roomTypeStr = roomTypeMatch[1];
        const roomType: RoomType = RoomType[roomTypeStr as keyof typeof RoomType];
//        console.log("Extracted roomTypeStr:", roomTypeStr);
//        console.log("Enum value names:", Object.keys(RoomType));
        if (!roomType) {
            console.log("roomtype not found ["+ roomTypeStr+"]");
        }

        return {
            lvl,
            x,
            y,
            flags,
            roomType
        };
    }

    static convert() {
        const extractedData = lineArray.map(this.extractDetails);
//        console.log(extractedData);
        const jsonData = JSON.stringify(extractedData, null, 2);
        const blob = new Blob([jsonData], { type: 'application/json' });
        const url = URL.createObjectURL(blob);

        const link = document.createElement('a');
        link.href = url;
        link.download = 'extracted_data.json';
        link.textContent = 'Download JSON';

        document.body.appendChild(link);
    }


}